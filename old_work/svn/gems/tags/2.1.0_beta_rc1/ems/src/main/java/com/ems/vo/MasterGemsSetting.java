package com.ems.vo;

public class MasterGemsSetting {
	String masterGemsIp ;
	String masterGemsPort;
	String emIp ;
	String emPort ;
	/**
	 * @return the masterGemsIp
	 */
	public String getMasterGemsIp() {
		return masterGemsIp;
	}
	/**
	 * @param masterGemsIp the masterGemsIp to set
	 */
	public void setMasterGemsIp(String masterGemsIp) {
		this.masterGemsIp = masterGemsIp;
	}
	/**
	 * @return the masterGemsPort
	 */
	public String getMasterGemsPort() {
		return masterGemsPort;
	}
	/**
	 * @param masterGemsPort the masterGemsPort to set
	 */
	public void setMasterGemsPort(String masterGemsPort) {
		this.masterGemsPort = masterGemsPort;
	}
	/**
	 * @return the emIp
	 */
	public String getEmIp() {
		return emIp;
	}
	/**
	 * @param emIp the emIp to set
	 */
	public void setEmIp(String emIp) {
		this.emIp = emIp;
	}
	/**
	 * @return the emPort
	 */
	public String getEmPort() {
		return emPort;
	}
	/**
	 * @param emPort the emPort to set
	 */
	public void setEmPort(String emPort) {
		this.emPort = emPort;
	}
	
	

}
